
class Calculator():

    def sum(self, first_value, second_value):
        return first_value + second_value

    def subtract(self, first_value, second_value):
        return first_value - second_value

    def mult(self, first_value, second_value):
        return first_value * second_value

    def div(self, first_value, second_value):
        return first_value / second_value

    def sqrt(self, value):
        return value